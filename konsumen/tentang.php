<p style="text-align: justify; color :  black;">PT Aneka Tambang Tbk adalah anak perusahaan BUMN pertambangan Inalum. PT Antam didirikan pada tanggal 5 Juli 1968. Kegiatan Antam mencakup eksplorasi, penambangan, pengolahan serta pemasaran dari sumber daya mineral.

Pendapatan PT Antam diperoleh melalui kegiatan eksplorasi dan penemuan deposit mineral, pengolahan mineral tersebut secara ekonomis, dan penjualan hasil pengolahan tersebut kepada konsumen jangka panjang yang loyal di Eropa dan Asia. Kegiatan ini telah dilakukan semenjak perusahaan berdiri tahun 1968. Komoditas utama Antam adalah bijih nikel kadar tinggi atau saprolit, bijih nikel kadar rendah atau limonit, feronikel, emas, perak dan bauksit. Jasa utama Antam adalah pengolahan dan pemurnian logam mulia serta jasa geologi.</p>

